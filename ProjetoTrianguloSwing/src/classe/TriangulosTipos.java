    private void bntVerificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntVerificarActionPerformed
        // TODO add your handling code here:
        // Slider ja entrega o valor como Inteiro 
        int a = sliA.getValue();
        int b = sliB.getValue();
        int c = sliC.getValue();

        // cada lado tem que ser menor que a soma dos outros dois
        if (a < b + c && b < a + c && c < a + b) {
            lblStatus.setText("Forma um triangulo");
            if (a == b && b == c) {
                lblTipo.setText("TRIANGULO EQUILATERO!");
            } else if (a != b && a != c && c != b) {
                lblTipo.setText("TRIANGULO ESCALENO!");
            } else {
                lblTipo.setText("TRIANGULO ISOCELES!");
            }

        } else {
            lblStatus.setText("N√£o Forma um triangulo");
            lblTipo.setText("-------------");
        }

        panResposta.setVisible(true);
    }//GEN-LAST:event_bntVerificarActionPerformed

