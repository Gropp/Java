    private void btnCalculaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalculaActionPerformed
        // TODO add your handling code here:
        int valor = Integer.parseInt(ctrlValor.getValue().toString());
        int resto = valor % 2;
        float cubo = (float) Math.pow(valor, 3);
        float sqrt = (float) Math.sqrt(valor);
        float rcubo = (float) Math.cbrt(valor);
        int abs = Math.abs(valor);
        lblNomeResto.setText("Resto da Divisao por 2 :");
        lblResResto.setText(Integer.toString(resto));
        lblNomeCubo.setText("Elevado ao Cubo :");
        lblResCubo.setText(String.format("%.2f",cubo));
        lblNomeSQR.setText("Raiz Quadrada :");
        lblResSQR.setText(String.format("%.2f",sqrt));
        lblNomeRaizCubo.setText("Raiz CÃºbica :");
        lblResRaizCubo.setText(String.format("%.2f",rcubo));
        lblNomeABS.setText("Valor Absoluto :");
        lblResABS.setText(Integer.toString(abs));        
        
    }//GEN-LAST:event_btnCalculaActionPerformed

